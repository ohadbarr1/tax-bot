#!/usr/bin/env node
/**
 * scripts/seed-fx-rates.mjs
 *
 * Backfill `data/fx/{usd,eur,gbp}_ils_daily.json` from the live Bank of Israel
 * public API. Idempotent — merges into the existing `rates` map without
 * deleting prior entries.
 *
 * Closes the data half of audit F-017 (`audits/tax-domain.md` §F-017):
 *   "FX uses annual mean (law: daily Bank-of-Israel rate per transaction)".
 *
 * Statutory anchor: סעיף 91(ג) לפקודת מס הכנסה ותקנות מס הכנסה (המרה למטבע
 * ישראלי) — שער יציג של יום העסקה.
 *
 * Usage:
 *   node scripts/seed-fx-rates.mjs                    # default 2020-01-01 → today
 *   node scripts/seed-fx-rates.mjs --from 2024-01-01  # custom start
 *   node scripts/seed-fx-rates.mjs --to   2024-12-31  # custom end
 *   node scripts/seed-fx-rates.mjs --currencies USD,EUR
 *   node scripts/seed-fx-rates.mjs --dry-run          # log without writing
 *
 * BoI API:
 *   GET https://www.boi.org.il/PublicApi/GetExchangeRates?asOfDate=YYYY-MM-DD
 *
 * Response shape (verified against live endpoint):
 *   {
 *     "exchangeRates": [
 *       { "key": "USD", "currentExchangeRate": 3.627, "currentChange": -0.05,
 *         "lastUpdate": "2024-03-14T16:00:00", "unit": 1, "name": "Dollar", ... },
 *       { "key": "EUR", ... },
 *       ...
 *     ]
 *   }
 *
 * The script tolerates both the older `currentExchangeRate` field name and
 * the more recent `rate` / `value` fallbacks observed on staging endpoints.
 */

import { readFile, writeFile, mkdir } from "node:fs/promises";
import { existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const DATA_DIR = join(__dirname, "..", "data", "fx");

const SUPPORTED = ["USD", "EUR", "GBP"];

// ─── CLI arg parser ──────────────────────────────────────────────────────────

function parseArgs(argv) {
  const args = { from: "2020-01-01", to: todayIso(), currencies: SUPPORTED, dryRun: false };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === "--from") args.from = argv[++i];
    else if (a === "--to") args.to = argv[++i];
    else if (a === "--currencies") args.currencies = argv[++i].split(",").map(s => s.trim().toUpperCase());
    else if (a === "--dry-run") args.dryRun = true;
    else if (a === "--help" || a === "-h") {
      console.log(`Usage: node scripts/seed-fx-rates.mjs [--from YYYY-MM-DD] [--to YYYY-MM-DD] [--currencies USD,EUR,GBP] [--dry-run]`);
      process.exit(0);
    }
  }
  return args;
}

function todayIso() {
  return new Date().toISOString().slice(0, 10);
}

function* iterateBusinessDays(from, to) {
  // Yield Sun–Thu only (BoI publication days). Fri = 5, Sat = 6 in JS.
  const start = new Date(from + "T00:00:00Z");
  const end = new Date(to + "T00:00:00Z");
  for (let d = new Date(start); d <= end; d.setUTCDate(d.getUTCDate() + 1)) {
    const dow = d.getUTCDay(); // 0=Sun .. 6=Sat
    if (dow === 5 || dow === 6) continue; // skip Fri/Sat
    yield d.toISOString().slice(0, 10);
  }
}

// ─── Network ─────────────────────────────────────────────────────────────────

async function fetchBoiRates(asOfDate) {
  const url = `https://www.boi.org.il/PublicApi/GetExchangeRates?asOfDate=${asOfDate}`;
  const res = await fetch(url, {
    headers: { Accept: "application/json", "User-Agent": "taxbot-seed-fx/1.0" },
    signal: AbortSignal.timeout(10_000),
  });
  if (!res.ok) {
    if (res.status === 404) return null; // no publication that day
    throw new Error(`BoI API ${res.status} for ${asOfDate}`);
  }
  const json = await res.json();
  const rows = json?.exchangeRates ?? json?.ExchangeRates ?? [];
  // Normalise into { USD: 3.62, EUR: 3.99, ... }
  const out = {};
  for (const row of rows) {
    const key = String(row.key ?? row.Key ?? "").toUpperCase();
    if (!SUPPORTED.includes(key)) continue;
    const rateRaw = row.currentExchangeRate ?? row.rate ?? row.value;
    const unit = Number(row.unit ?? row.Unit ?? 1) || 1;
    const rate = Number(rateRaw);
    if (!Number.isFinite(rate) || rate <= 0) continue;
    // BoI quotes some currencies per 100 units (e.g. JPY); normalise to per-unit.
    out[key] = unit === 1 ? rate : rate / unit;
  }
  return out;
}

// ─── Disk I/O ────────────────────────────────────────────────────────────────

function fileFor(currency) {
  return join(DATA_DIR, `${currency.toLowerCase()}_ils_daily.json`);
}

async function readDataset(currency) {
  const path = fileFor(currency);
  if (!existsSync(path)) {
    return {
      currency,
      base: "ILS",
      source: "Bank of Israel — https://www.boi.org.il/PublicApi/GetExchangeRates",
      _warning: "Generated by scripts/seed-fx-rates.mjs",
      annualMean: {},
      rates: {},
    };
  }
  const raw = await readFile(path, "utf-8");
  return JSON.parse(raw);
}

async function writeDataset(currency, ds) {
  await mkdir(DATA_DIR, { recursive: true });
  const path = fileFor(currency);
  const sorted = {};
  for (const k of Object.keys(ds.rates).sort()) sorted[k] = ds.rates[k];
  ds.rates = sorted;
  await writeFile(path, JSON.stringify(ds, null, 2) + "\n", "utf-8");
}

// ─── Main ────────────────────────────────────────────────────────────────────

async function main() {
  const args = parseArgs(process.argv.slice(2));
  console.log(`[seed-fx] ${args.from} → ${args.to}, currencies: ${args.currencies.join(",")} (dryRun=${args.dryRun})`);

  const datasets = {};
  for (const c of args.currencies) datasets[c] = await readDataset(c);

  let added = 0, skipped = 0, failed = 0, processed = 0;
  for (const date of iterateBusinessDays(args.from, args.to)) {
    processed++;
    // Skip if all requested currencies are already present for this date.
    const allPresent = args.currencies.every(c => typeof datasets[c].rates[date] === "number");
    if (allPresent) { skipped++; continue; }
    try {
      const rates = await fetchBoiRates(date);
      if (!rates) { skipped++; continue; }
      for (const c of args.currencies) {
        if (typeof rates[c] === "number" && typeof datasets[c].rates[date] !== "number") {
          datasets[c].rates[date] = Number(rates[c].toFixed(4));
          added++;
        }
      }
      // Be polite to the BoI server — 100ms between requests.
      await new Promise(r => setTimeout(r, 100));
    } catch (err) {
      failed++;
      console.warn(`[seed-fx] ${date}: ${err.message}`);
    }
    if (processed % 50 === 0) {
      console.log(`[seed-fx] processed ${processed} business days; added ${added}, skipped ${skipped}, failed ${failed}`);
    }
  }

  console.log(`[seed-fx] done. added=${added} skipped=${skipped} failed=${failed} totalDates=${processed}`);

  if (args.dryRun) {
    console.log(`[seed-fx] --dry-run set; not writing files.`);
    return;
  }

  for (const c of args.currencies) {
    await writeDataset(c, datasets[c]);
    const count = Object.keys(datasets[c].rates).length;
    console.log(`[seed-fx] wrote ${fileFor(c)} (${count} dates total)`);
  }
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
